#include <iostream>

using namespace std;
int main()
{
setlocale(LC_ALL, "Russian");
int a = 1, sum = 0; // Задаем переменные
while (a <= 1000) // Задаем числа
{
    a = a + 4;
    if (a <= 1000) // Проверяем, является ли число a меньше 1000 (если не ставить это ограничение, то программа будет плюсовать 1001)
    if (a % 7 ==0) // Проверяем делимость на 7
    {
        sum = sum + a; // Если делится без остатка, добавляем это число к сумме
    }
    else
    {
        sum = sum; // Если делится с остатком, оставляем значение суммы неизменным
    }
}
cout << "Сумма чисел равна: " << sum;
return 0;
}
